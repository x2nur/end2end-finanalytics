df_users = df_users.withColumn('current_age', F.year(F.current_date()) - F.col('birth_year') - F.when(F.month(F.current_date()) - F.col('birth_month') >= 0, 0).otherwise(1) )

# time left until retirement 
df_users = df_users.withColumn('is_retired', F.col('current_age') >= F.col('retirement_age'))

