# replace $ in the amount value
df_tx = ( 
    df_tx
    .withColumn(
        'amount', 
        F.regexp_replace('amount', r'\$', '').cast(T.FloatType() )
    )
)


# check merchant_state
df_tx_empty_state = df_tx.where(F.col('merchant_state').isNull())
df_tx_empty_state.select('merchant_city').distinct().show()
# fix
df_tx = (
    df_tx
    .withColumn(
        'merchant_state', 
        F.when(col('merchant_city') == 'ONLINE', 'ONLINE')
            .otherwise(col('merchant_state') ) 
    )
)

# check errors
df_tx.select(F.explode(F.split('errors', ','))).distinct().show()
# fix
df_tx = df_tx.fillna('NO ERROR', subset=['errors'])


# fix zip 
df_tx: DataFrame = df_tx.withColumn('zip', F.col('zip').cast(T.StringType()))
# step 1
df_tx = (
    df_tx
    .withColumn(
        'zip', 
        F.when(col('merchant_city') == 'ONLINE', 'N/A')
        .otherwise(col('zip'))
    )
)

# update N/A 
# step 2. from equivalent city/state
win = Window.partitionBy('merchant_city','merchant_state')
df_tx = df_tx.withColumn('zip', F.first_value('zip', ignoreNulls=True).over(win))
# df_tx.where(F.col('zip').isNull()).count()

# step 3.
# find all tx where zip is null
df_tx_empty_zip = df_tx.where(F.col('zip').isNull())
# save city,state w/ nullable zip 
df_tx_empty_zip_merch = (
    df_tx_empty_zip
    .where(col('merchant_city') != 'ONLINE')
    .select('merchant_city', 'merchant_state')
    .distinct()
)
df_tx_empty_zip_merch.coalesce(1).write.csv('file:///home/hadoop/workspace/output/empty_zip.csv')


